def predict_state(aggregates):
    """
    Предсказывает состояние по признакам модели.
    """
    # Передаём numpy массив (без имён колонок)
    X = np.array([[aggregates[f] for f in feature_names]])
    
    pred_idx = int(model.predict(X)[0])
    probas = model.predict_proba(X)[0]
    state_name = class_names[pred_idx]
    confidence = float(np.max(probas))
    
    if confidence < confidence_threshold:
        state_name = f"НЕОПРЕДЕЛЁННО ({state_name})"
    
    return state_name, probas, confidence


