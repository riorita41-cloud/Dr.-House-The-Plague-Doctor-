class AggregateFilter:
    def __init__(self, size=3):
        self.size = size
        self.buffers = {feat: deque(maxlen=size) for feat in feature_names}
    
    def add(self, aggregates_dict):
        for feat in feature_names:
            if feat in aggregates_dict:
                self.buffers[feat].append(aggregates_dict[feat])
            else:
                self.buffers[feat].append(0.0)
    
    def get_filtered(self):
        result = {}
        for feat in feature_names:
            vals = list(self.buffers[feat])
            result[feat] = round(sum(vals) / len(vals), 2)
        return result


