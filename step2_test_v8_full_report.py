def clamp(x, lo, hi):
    return max(lo, min(hi, x))

def generate_window_packets(state_name, n_packets=TOTAL_PACKETS):
    """
    Генерирует окно из n_packets значений для каждого канала.
    """
    p = TEST_STATES.get(state_name, TEST_STATES["Норма"])
    window = {ch: [] for ch in CHANNELS if ch in p}
    
    # Если канал есть в модели но нет в TEST_STATES — заполняем нулями
    for ch in CHANNELS:
        if ch not in window:
            window[ch] = [0.0] * n_packets
    
    # Тренд (некоторые состояния имеют нарастание)
    trends = {ch: random.uniform(-0.02, 0.02) for ch in CHANNELS}
    
    for i in range(n_packets):
        for ch in CHANNELS:
            if ch in p:
                mu, sigma = p[ch]
                t_factor = 1.0 + trends[ch] * (i / n_packets)
                noise = random.gauss(0, sigma)
                value = mu * t_factor + noise
                value = clamp(value, 0, 200 if ch == "пульс" else 100)
            else:
                value = 0.0
            window[ch].append(value)
    
    return window

def compute_aggregates(window):
    """
    Вычисляет признаки из окна ТОЧНО как в модели.
    """
    features = {}
    
    for feat in feature_names:
        # Разбираем имя признака: "пульс_mean" → ch="пульс", agg="mean"
        parts = feat.rsplit("_", 1)
        if len(parts) != 2:
            features[feat] = 0.0
            continue
        
        ch, agg = parts
        
        if ch not in window or len(window[ch]) == 0:
            features[feat] = 0.0
            continue
        
        values = np.array(window[ch])
        
        if agg == "mean":
            features[feat] = round(float(np.mean(values)), 2)
        elif agg == "std":
            features[feat] = round(float(np.std(values)), 2)
        elif agg == "min":
            features[feat] = round(float(np.min(values)), 2)
        elif agg == "max":
            features[feat] = round(float(np.max(values)), 2)
        elif agg == "slope":
            # Линейный тренд (как в модели)
            if len(values) > 1:
                x = np.arange(len(values))
                slope = np.polyfit(x, values, 1)[0]
                features[feat] = round(float(slope), 4)
            else:
                features[feat] = 0.0
        else:
            features[feat] = 0.0
    
    return features

def generate_test_sample(state_name):
    """
    Генерирует один тестовый сэмпл (точно как модель ожидает).
    """
    window = generate_window_packets(state_name)
    aggregates = compute_aggregates(window)
    return aggregates, window


